


    function grade(n) {
        if (n < 60) {
            console.log("F");
        } else if (n < 70) {
            console.log("D");
        } else if (n < 80) {
            console.log("C");
        } else if (n < 90) {
            console.log("B");
        } else if (n <= 100) {
            console.log("A");
        } else {
            console.log("n");
        }
    }
    
    grade(45);
    
    